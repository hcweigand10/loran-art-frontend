export default (id: number) => {
  switch (id) {
    case 1:
      return "Wall Art";
    case 2:
      return "Sculptures";
    case 3:
      return "Toys";
    case 4:
      return "Whistles";
    case 5:
      return "Planes";
    case 6:
      return "Wholesale";
    default:
      return "Wall Art";
  }
};
